            <p>Banda Aceh (Waspada Aceh) – PT Bank Syariah Indonesia Tbk (BSI) bersama BSI Maslahat menjalin kerja sama dengan Atsiri Research Center (ARC) Universitas Syiah Kuala (ASK), untuk penguatan kapasitas pelaku usaha budidaya nilam di wilayah Aceh.</p>

            <p>Kerja sama ini juga diharapkan bisa meningkatkan kualitas minyak nilam Aceh yang merupakan komoditas unggulan Indonesia dan mendukung pemulihan ekonomi melalui program pemberdayaan.</p>

            <p>Direktur Sales & Distribution Bank BSI, Anton Sukarna, mengatakan dalam kerja sama ini, BSI dan BSI Maslahat berperan melaksanakan program UMKM BSI bagi masyarakat pelaku budidaya nilam di wilayah Aceh.</p>

            <p>Sementara ARC Universitas Syiah Kuala memberikan dukungan agar program UMKM BSI Klaster Minyak Nilam dapat berhasil dan kualitas produk minyak nilam dapat meningkat. Yakni dengan melakukan pendampingan.</p>

            <p>“Harapannya ARC Universitas Syiah Kuala mendampingi dan menyerap hasil panen minyak nilam dari petani binaan BSI Maslahat. Yaitu masyarakat yang kurang mampu/mustahik. Dari satu hektare lahan biasanya mampu menghasilkan 200 kg nilam,” kata Anton, dalam keterangannya kepada waspadaaceh.com, Selasa (27/9/2022).</p>

            <p>Anton mengatakan adapun harga minyak nilam saat ini mencapai Rp500 ribu – Rp600 ribu per Kg. Lahan eksisting saat ini mencapai 15 hektare. Sementara jumlah penerima manfaatnya ada 50 orang.</p>

            <p>ARC Universitas Syiah Kuala merupakan lembaga yang didirikan untuk mengangkat kembali minyak nilam yang merupakan komoditas unggulan Aceh. Minyak nilam sendiri memiliki sejarah panjang di Aceh sejak zaman Belanda.</p>

            <p>Rencananya BSI dan BSI Maslahat akan menyiapkan program khusus untuk mengangkat potensi komoditas nilam Aceh. Produk minyak nilam tersebut saat ini dapat dijumpai di UMKM Center BSI di Aceh.</p>

            <p>Pada tahun 2022 ini, BSI menargetkan produk minyak nilam asal Aceh dapat menjadi komoditas ekspor asal Aceh.</p>

            <p>“BSI Maslahat akan terus berkolaborasi  dengan ARC Universitas Syiah Kuala dan kembali menghidupkan gairah produksi minyak nilam. Harapannya dengan perjanjian kerja sama dengan Univesitas Syiah Kuala melalui ARC dapat mendampingi petani nilam agar mampu bersaing di pasar,” jelasnya. (*)</p>
